    public class DateTimeUiOptions : PropertyUiInfo
    {
        public DateTimeUiOptions(string type, string dataTimezone, string displayTimezone) : base(type)
        {
            this.DataTimezone = dataTimezone;
            this.DisplayTimezone = displayTimezone;
        }

        public override string CreateAttribute()
        {
            var sb = new StringBuilder();
            sb.Append("[DateTimeUiType(");
            bool needComma = false;
            if(DataTimezone != null)
            {
                sb.Append($"dataTimezone: {DateTimeUiTypeAttribute.Timezones.GetTimezoneCodeName(DataTimezone)}");
                needComma = true;
            }
            if(DisplayTimezone != null)
            {
                if (needComma)
                {
                    sb.Append(", ");
                }
                sb.Append($"displayTimezone: {DateTimeUiTypeAttribute.Timezones.GetTimezoneCodeName(DisplayTimezone)}");
            }
            sb.Append($"{AddSharedProperties(needComma)})]");
            return sb.ToString();
        }

        public String DataTimezone { get; set; }

        public String DisplayTimezone { get; set; }
    }

